    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        Peca p = new Peca();
        p.setNome(txtNome.getText());
        p.setQuantidade(Integer.parseInt(txtQuantidade1.getText()));
        p.setValorUnitario(Double.parseDouble(txtValorUnitario.getText()));
        p.setFgAtivo(cbxAtivo.getSelectedIndex() == 0 ? 1 : 0);
        
        int res;
        if (txtId.getText().isEmpty()){
            res = new PecaDAO().inserir(p);
        }else{
            p.setId(Integer.parseInt(txtId.getText()));
            res = new PecaDAO().atualizar(p);
        }

        if (res > 0){
            txtId.setText(String.valueOf(res));
            JOptionPane.showMessageDialog(null,"Operação realizada com sucesso!");
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null,"Não foi possível realizar a operação.");
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

