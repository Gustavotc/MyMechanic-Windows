    //Construtor para editar Peça
    public FormCadastroPeca(Object obj) { 
        this();
        Peca p = (Peca) obj;
        if( p != null) {
            txtId.setText(Integer.toString(p.getId()));
            txtNome.setText(p.getNome());
            txtQuantidade1.setText(Integer.toString(p.getQuantidade()));
            txtValorUnitario.setText(Double.toString(p.getValorUnitario()));
            cbxAtivo.setSelectedIndex(p.getFgAtivo()==0 ? 1 : 0);
        }
    }
    
